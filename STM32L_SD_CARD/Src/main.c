HAL_StatusTypeDef User_SD_Init(void)
{
			
		/*##-1- Link the micro SD disk I/O driver ##################################*/
		res = FATFS_LinkDriver(&SD_Driver, SDPath);
		if(res == 0)
		{
			/*##-2- Register the file system object to the FatFs module ##############*/
			res = f_mount(&SDFatFS, (TCHAR const*)SDPath, 0);
			if(res != FR_OK)
			{
				/* FatFs Initialization Error */
				_Error_Handler(" SD Card mount Error", __LINE__);
			}
			else
			{
				return HAL_OK ;
			}
			
		}
}
	

HAL_StatusTypeDef User_SD_File_Write(uint8_t filename[], uint8_t write_buffer[])
{
	res  = f_open(&SDFile, filename, FA_CREATE_ALWAYS | FA_WRITE);
		if(res != FR_OK)
				{
					/* 'STM32.TXT' file Open for write Error */
					_Error_Handler(" File Open Error", __LINE__);
				}
		else
		{
				/*##-5- Write data to the text file ################################*/
				res = f_write(&SDFile, write_buffer, sizeof(*write_buffer), (void *)&byteswritten);
				
				if((byteswritten == 0) || (res != FR_OK))
				{
					/* 'STM32.TXT' file Write or EOF Error */
					_Error_Handler(" File Write Error", __LINE__);
				}
				else
				{
						/*##-6- Close the open text file #################################*/
						return HAL_OK ;
						f_close(&SDFile);
				}
			}
}


